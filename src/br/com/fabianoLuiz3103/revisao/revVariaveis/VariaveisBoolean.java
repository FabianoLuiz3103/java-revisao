package br.com.fabianoLuiz3103.revisao.revVariaveis;

public class VariaveisBoolean {

    public static void main(String[] args) {

        boolean verdadeiro = 2 > 1;
        boolean falso = 1 > 2;

        System.out.println("2 > 1 --> " + verdadeiro);

        System.out.println("2 < 1 --> " + falso);
    }
}
