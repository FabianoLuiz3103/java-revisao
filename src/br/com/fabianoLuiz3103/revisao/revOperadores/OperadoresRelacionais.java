package br.com.fabianoLuiz3103.revisao.revOperadores;

public class OperadoresRelacionais {

    public static void main(String[] args) {

        int valorUm = 1;
        int valorDois = 2;

        System.out.println("ValorUm == ValorDois: " + (valorUm == valorDois));
        System.out.println("ValorUm != ValorDois: " + (valorUm != valorDois));
        System.out.println("ValorUm > ValorDois: " + (valorUm > valorDois));
        System.out.println("ValorUm >= ValorDois: " + (valorUm >= valorDois));
        System.out.println("ValorUm < ValorDois: " + (valorUm < valorDois));
        System.out.println("ValorUm <= ValorDois: " + (valorUm <= valorDois));
    }
}
