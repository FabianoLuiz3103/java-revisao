package br.com.fabianoLuiz3103.exercicios.lista010.exercicio03;

public class ConversaoDeUnidadesDeArea {

    public static double metrosToPes(double metros){
        return (metros*10.76);
    }

    public static double pesToCentimetros(double pes){
        return (pes*926);
    }

    public static double milhasToAcres(double milhas){
        return (milhas*640);
    }

    public static double acresToPes(double acres){
        return (acres*43560);
    }
}
